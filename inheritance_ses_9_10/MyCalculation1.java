package inheritance_ses_9_10;

public class MyCalculation1 {

	int z;
	
	public void Multiplication(int x, int y) {
		z = x*y;
		System.out.println("x*y = " + z);
	}
	public void Distraction(int x, int y) {
		z = x-y;
		System.out.println("x-y = " + z);
	}
	
	public void Sum(int x, int y) {
	z = x+y;
	System.out.println("x+y = " + z);
	}
}

